	public static SuspensionPart GetSuspension(VehicleType _vehicleType, string suspensionName)
	{
		SuspensionPart suspensionPart = Suspensions.suspensions.Find((SuspensionPart s) => s.vehicleType == _vehicleType && s.partName == suspensionName);
		if (suspensionPart == null)
		{
			suspensionPart = Suspensions.suspensions.Find((SuspensionPart s) => s.vehicleType == VehicleType.Any && s.partName == suspensionName);
		}
		return suspensionPart;
	}

	public static SuspensionUpgrade GetSuspensionUpgrade(string suspensionName, int stage)
	{
		return Suspensions.suspensionUpgrades.Find((SuspensionUpgrade u) => u.SuspensionName == suspensionName && u.Stage == stage);
	}

	public static WheelsUpgrade GetWheelsUpgrade(int stage)
	{
		return Suspensions.wheelsUpgrades.Find((WheelsUpgrade u) => u.Stage == stage);
	}

